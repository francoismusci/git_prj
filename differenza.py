def sottrazione(a, b):
    """
    Funzione per sottrarre due numeri.
    
    Args:
        a (int): Il minuendo.
        b (int): Il sottraendo.
    
    Returns:
        int: Il risultato della sottrazione.
    """
    return a - b
differenza = sottrazione(10, 5)
